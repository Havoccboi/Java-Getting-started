package beginnerJava;

public class AssignmentDemo {
	
	public static void main(String [] args) {
		
				int x=50;
				
				int y=25;
				
				x+=y;
				
				System.out.println("Result : "+x);
				
				x-=y;
				
				System.out.println("Result : "+x);
				
				x/=y;
				
				System.out.println("Result : "+x);
				
				x*=y;
				
				System.out.println("Result : "+x);
				
				x%=y;
				
				System.out.println("Result : "+x);
		
	}

}
