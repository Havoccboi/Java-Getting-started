package beginnerJava;

public class BitwiseOperator {
	
	public static void main(String[] args) {

	int a=32;
	
	int b=12;
	
	int c;
	
	c=a&b;
	
	System.out.println("Value of c is : "+c);
	
	c=a|b;
	
	System.out.println("Value of c is :  "+c);
	
	c=a^b;
	
	System.out.println("Value of c is : "+c);
	
	c= a >>3;
	
	System.out.println("Value of c is : "+c);
	
	c=a<<3;
	
	System.out.println("Value of c is  : "+c); 
		
	
	
}

	
}